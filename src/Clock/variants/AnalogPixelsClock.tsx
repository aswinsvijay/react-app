const AnalogPixel: React.FC<{ n1: number; n2: number }> = ({ n1, n2 }) => {
  return (
    <div className="pixel-container">
      <div className="square">
        {[n1, n2].map((value) => {
          return (
            <div
              className={`needle`}
              style={{ transform: `translate(-50%, 0%) rotate(calc(${(value + 6) * 30}deg))` }}
            ></div>
          );
        })}
      </div>
    </div>
  );
};

const AnalogPixelDigit: React.FC<{ digit: number }> = ({ digit }) => {
  return (
    <div className="pixel-grid">
      {digitMap[digit].map(([n1, n2]) => {
        return <AnalogPixel n1={n1} n2={n2} />;
      })}
    </div>
  );
};

export const AnalogPixelsClock: React.FC<ClockComponentProps> = ({ timeParts }) => {
  return (
    <div className="analog-pixels-clock-container">
      {timeParts.map((digit) => (
        <AnalogPixelDigit digit={digit} />
      ))}
    </div>
  );
};
